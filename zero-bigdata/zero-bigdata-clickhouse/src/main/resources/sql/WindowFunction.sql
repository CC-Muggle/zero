    ('sku1', '2020-03-01', 1);

-- 开窗函数即，给分析内容确定一个区间，在这个区间内计算
-- 下边sql执行语义为，按照分区，逐个向后加和
SELECT
    item,
    ts,
    value,
    sum(value) OVER (PARTITION BY item ORDER BY ts ASC) AS stock_balance
FROM warehouse
ORDER BY
    item ASC,
    ts ASC;